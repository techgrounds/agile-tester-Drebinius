

    /*
    ACCESS CONTROL MODIFIERS

OPDR1
    1. PRIVATE
    access level is alleen in dezelfde class, kan niet buiten de dezelfde class om aangeschreven worden

    2. DEFAULT
      access level binnen dezelfde package, is de standaard access level unless otherwise specified

    3.PROTECTED
        access level binnen de package, en buiten de package dmv een child class, zonder deze child class kan dit niet
        buiten de package om aanschreven worden

    4.PUBLIC
        access level overal, kan van overal aangeschreven worden. binnen/buiten class/package


OPDR2

    1.STATIC
        static wordt gebruikt voor variables die elke instance van een bepaalde class hebben.
        in een database die de DoB, username en hobbies van gebruikers bijhoudt, kan er bijv een class ''User'' zijn.
        omdat elke gebruiker tot deze class zou behoren, wil je dat deze class standaard een aantal variables mee krijgt.

    2.FINAL
        gebruik indien je niet wilt dat de waarde nog aangepast gaat worden. Stel dat er een geboortedatum staat vastgelegd
        in een database, deze zal nooit veranderen, en dus is het gebruik van final gepast.


OPDR3

    1. .equals() gebruik je om String values te vergelijken

    2. == wordt gebruikt om integers/numerieke waarden  te vergelijken

     */

